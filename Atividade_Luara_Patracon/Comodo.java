public class Comodo {
	//atributos
	private String nome;
	
	//construtores
	public Comodo() {
		
	}
	public Comodo(String nome) {
		this.nome = nome;
	}
	
	//getters e setters
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome=nome;
	}
}
